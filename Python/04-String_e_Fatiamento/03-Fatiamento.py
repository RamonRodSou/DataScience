# Fatiamento de string é uma técnica utilizada para retornar substrings
# (partes da string original), informando inicio (start),
# fim(stop) e passo (step): [start: stop[,step]].

nome = "Ramon Rodrigues de Souza"


print(nome[0])
print(nome[:9])
print(nome[10:16])
print(nome[10:16:2])
print(nome[:])
print(nome[::-1]) # espelhamento
print(nome[:-1])
print(nome[:-3])




